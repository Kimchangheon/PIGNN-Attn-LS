#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
inspect_generated_parquet.py

Inspect and sanity-check parquet files generated by your new
branch-row direct-SI pandapower dataset generator.

Usage examples
--------------
# inspect first row
python inspect_generated_parquet.py --path ./data/case14_ppcY_1000_NR_branchrows_directSI.parquet

# inspect several rows
python inspect_generated_parquet.py --path=./case14_ppcY_36000_NR_branchrows_directSI.parquet--rows 0 1 2 10

# inspect random rows
python inspect_generated_parquet.py --path=case14_ppcY_36000_NR_branchrows_directSI.parquet --random_rows 3

# skip Y reconstruction
python inspect_generated_parquet.py --path ./data/file.parquet --no_check_y
"""

import io
import argparse
from typing import Any, Dict, List

import numpy as np
import pyarrow.parquet as pq


# ============================================================
# Helpers
# ============================================================

def npy_bytes_to_ndarray(b: bytes) -> np.ndarray:
    return np.load(io.BytesIO(b), allow_pickle=False)


def safe_decode(row: Dict[str, Any], key: str):
    val = row.get(key, None)
    if val is None:
        return None
    if isinstance(val, (bytes, bytearray, memoryview)):
        return npy_bytes_to_ndarray(bytes(val))
    return val


def reconstruct_Y_pandapower_branchrows_direct_SI(
    N: int,
    Branch_f_bus: np.ndarray,
    Branch_t_bus: np.ndarray,
    Branch_status: np.ndarray,
    Branch_tau: np.ndarray,
    Branch_shift_deg: np.ndarray,
    Branch_y_series_from: np.ndarray,
    Branch_y_series_to: np.ndarray,
    Branch_y_series_ft: np.ndarray,
    Branch_y_shunt_from: np.ndarray,
    Branch_y_shunt_to: np.ndarray,
    Y_shunt_bus: np.ndarray,
) -> np.ndarray:
    """
    Direct SI reconstruction from one metadata row per PPC branch row.
    """
    Y_SI = np.zeros((N, N), dtype=np.complex128)

    # bus shunts
    Y_SI[np.diag_indices(N)] += Y_shunt_bus.astype(np.complex128)

    nl = len(Branch_f_bus)
    for k in range(nl):
        if int(Branch_status[k]) == 0:
            continue

        f = int(Branch_f_bus[k])
        t = int(Branch_t_bus[k])

        tau = float(Branch_tau[k])
        theta = np.deg2rad(float(Branch_shift_deg[k]))
        a = tau * np.exp(1j * theta)

        y_from = Branch_y_series_from[k]
        y_to   = Branch_y_series_to[k]
        y_ft   = Branch_y_series_ft[k]
        ysh_f  = Branch_y_shunt_from[k]
        ysh_t  = Branch_y_shunt_to[k]

        Yff = (y_from + ysh_f / 2.0) / (a * np.conj(a))
        Ytt = (y_to   + ysh_t / 2.0)
        Yft = -y_ft / np.conj(a)
        Ytf = -y_ft / a

        Y_SI[f, f] += Yff
        Y_SI[t, t] += Ytt
        Y_SI[f, t] += Yft
        Y_SI[t, f] += Ytf

    return Y_SI


def expected_keys() -> List[str]:
    return [
        "bus_number",
        "branch_number",
        "gridtype",
        "U_base",
        "S_base",

        "bus_typ",
        "vn_kv",
        "Y_shunt_bus",

        "Branch_f_bus",
        "Branch_t_bus",
        "Branch_status",
        "Branch_tau",
        "Branch_shift_deg",
        "Branch_y_series_from",
        "Branch_y_series_to",
        "Branch_y_series_ft",
        "Branch_y_shunt_from",
        "Branch_y_shunt_to",
        "Is_trafo",
        "Branch_hv_is_f",
        "Branch_n",

        "Y_Lines",
        "Y_C_Lines",

        "u_start",
        "u_newton",
        "S_start",
        "S_newton",

        # optional
        "Y_matrix",
    ]


# ============================================================
# Validation / inspection
# ============================================================

def validate_shapes(sample: Dict[str, Any]) -> List[str]:
    errs = []

    N = int(sample["bus_number"])
    nl = int(sample["branch_number"])

    # per-bus arrays
    for name in ["bus_typ", "vn_kv", "Y_shunt_bus", "u_start", "u_newton", "S_start", "S_newton"]:
        arr = sample.get(name, None)
        if arr is None:
            errs.append(f"missing {name}")
            continue
        if len(arr) != N:
            errs.append(f"{name}: len={len(arr)} but bus_number={N}")

    # per-branch arrays
    branch_names = [
        "Branch_f_bus", "Branch_t_bus", "Branch_status",
        "Branch_tau", "Branch_shift_deg",
        "Branch_y_series_from", "Branch_y_series_to", "Branch_y_series_ft",
        "Branch_y_shunt_from", "Branch_y_shunt_to",
        "Is_trafo", "Branch_hv_is_f", "Branch_n",
        "Y_Lines", "Y_C_Lines"
    ]
    for name in branch_names:
        arr = sample.get(name, None)
        if arr is None:
            errs.append(f"missing {name}")
            continue
        if len(arr) != nl:
            errs.append(f"{name}: len={len(arr)} but branch_number={nl}")

    # bus index range
    if "Branch_f_bus" in sample and "Branch_t_bus" in sample:
        fb = sample["Branch_f_bus"]
        tb = sample["Branch_t_bus"]
        if np.any(fb < 0) or np.any(fb >= N):
            errs.append("Branch_f_bus has out-of-range indices")
        if np.any(tb < 0) or np.any(tb >= N):
            errs.append("Branch_t_bus has out-of-range indices")

    # Y_matrix shape if present
    if "Y_matrix" in sample and sample["Y_matrix"] is not None:
        Y = sample["Y_matrix"]
        if Y.shape != (N, N):
            errs.append(f"Y_matrix shape={Y.shape}, expected ({N},{N})")

    return errs


def inspect_one_sample(sample: Dict[str, Any], row_idx: int, check_y: bool = True):
    N = int(sample["bus_number"])
    nl = int(sample["branch_number"])

    print("=" * 80)
    print(f"ROW {row_idx}")
    print(f"gridtype     : {sample['gridtype']}")
    print(f"bus_number   : {N}")
    print(f"branch_number: {nl}")
    print(f"U_base       : {sample['U_base']}")
    print(f"S_base       : {sample['S_base']}")

    # summary counts
    is_trafo = sample["Is_trafo"]
    n_trafo = int(np.sum(is_trafo != 0))
    n_active = int(np.sum(sample["Branch_status"] != 0))
    print(f"active branches : {n_active}")
    print(f"trafo branches  : {n_trafo}")
    print(f"line branches   : {nl - n_trafo}")

    # quick voltage / power summaries
    u_start = sample["u_start"]
    u_newton = sample["u_newton"]
    s_start = sample["S_start"]
    s_newton = sample["S_newton"]

    print(f"|u_start| min/max : {np.abs(u_start).min():.6g} / {np.abs(u_start).max():.6g}")
    print(f"|u_newton| min/max: {np.abs(u_newton).min():.6g} / {np.abs(u_newton).max():.6g}")
    print(f"P_start sum [MW]  : {np.real(s_start).sum() / 1e6:.6g}")
    print(f"Q_start sum [MVAr]: {np.imag(s_start).sum() / 1e6:.6g}")
    print(f"P_newton sum [MW] : {np.real(s_newton).sum() / 1e6:.6g}")
    print(f"Q_newton sum [MVAr]: {np.imag(s_newton).sum() / 1e6:.6g}")

    # shape checks
    errs = validate_shapes(sample)
    if errs:
        print("\n[SHAPE / CONSISTENCY ERRORS]")
        for e in errs:
            print(" -", e)
    else:
        print("\n[SHAPE CHECK] OK")

    # reconstruct Y
    if check_y:
        Y_rec = reconstruct_Y_pandapower_branchrows_direct_SI(
            N=N,
            Branch_f_bus=sample["Branch_f_bus"],
            Branch_t_bus=sample["Branch_t_bus"],
            Branch_status=sample["Branch_status"],
            Branch_tau=sample["Branch_tau"],
            Branch_shift_deg=sample["Branch_shift_deg"],
            Branch_y_series_from=sample["Branch_y_series_from"],
            Branch_y_series_to=sample["Branch_y_series_to"],
            Branch_y_series_ft=sample["Branch_y_series_ft"],
            Branch_y_shunt_from=sample["Branch_y_shunt_from"],
            Branch_y_shunt_to=sample["Branch_y_shunt_to"],
            Y_shunt_bus=sample["Y_shunt_bus"],
        )

        if sample.get("Y_matrix", None) is not None:
            Y = sample["Y_matrix"]
            diff = np.max(np.abs(Y_rec - Y))
            print(f"\n[Y CHECK] max |Y_rec - Y_matrix| = {diff:.12g}")
            print(f"[Y CHECK] allclose = {np.allclose(Y_rec, Y, rtol=1e-9, atol=1e-9)}")
        else:
            print("\n[Y CHECK] skipped compare because Y_matrix is not stored")

    # show first few branch rows
    print("\n[FIRST 5 BRANCH ROWS]")
    show = min(5, nl)
    for k in range(show):
        print(
            f"  k={k:3d}  "
            f"f={int(sample['Branch_f_bus'][k]):4d}  "
            f"t={int(sample['Branch_t_bus'][k]):4d}  "
            f"status={int(sample['Branch_status'][k])}  "
            f"is_trafo={int(sample['Is_trafo'][k])}  "
            f"tau={float(sample['Branch_tau'][k]):.6g}  "
            f"shift={float(sample['Branch_shift_deg'][k]):.6g}"
        )


def read_row_as_decoded_dict(table, row_idx: int) -> Dict[str, Any]:
    rec = table.slice(row_idx, 1).to_pylist()[0]

    out = {}
    for k, v in rec.items():
        if isinstance(v, (bytes, bytearray, memoryview)):
            out[k] = npy_bytes_to_ndarray(bytes(v))
        else:
            out[k] = v
    return out


# ============================================================
# Main
# ============================================================

def parse_args():
    ap = argparse.ArgumentParser(description="Inspect parquet generated by branch-row pandapower dataset generator.")
    ap.add_argument("--path", type=str, required=True, help="Path to parquet file")
    ap.add_argument("--rows", type=int, nargs="*", default=None, help="Specific row indices to inspect")
    ap.add_argument("--random_rows", type=int, default=0, help="Inspect this many random rows")
    ap.add_argument("--seed", type=int, default=0)
    ap.add_argument("--no_check_y", action="store_true", help="Skip Y reconstruction/compare")
    return ap.parse_args()


def main():
    args = parse_args()

    pf = pq.ParquetFile(args.path)
    print("=" * 80)
    print("PARQUET FILE")
    print(f"path     : {args.path}")
    print(f"rows     : {pf.metadata.num_rows}")
    print(f"rowgroups: {pf.num_row_groups}")
    print("\nSCHEMA")
    print(pf.schema_arrow)

    # read all columns once; simple and convenient for inspection
    table = pq.read_table(args.path)

    names = set(table.column_names)
    missing = [k for k in expected_keys() if (k != "Y_matrix" and k not in names)]
    if missing:
        print("\n[WARNING] Missing expected columns:")
        for m in missing:
            print(" -", m)

    nrows = table.num_rows
    row_ids = []

    if args.rows is not None and len(args.rows) > 0:
        row_ids.extend(args.rows)

    if args.random_rows > 0:
        rng = np.random.default_rng(args.seed)
        row_ids.extend(rng.choice(nrows, size=min(args.random_rows, nrows), replace=False).tolist())

    if not row_ids:
        row_ids = [0]

    # deduplicate and keep valid
    row_ids = sorted(set(i for i in row_ids if 0 <= i < nrows))

    print(f"\nInspecting rows: {row_ids}\n")

    for idx in row_ids:
        sample = read_row_as_decoded_dict(table, idx)
        inspect_one_sample(sample, idx, check_y=not args.no_check_y)


if __name__ == "__main__":
    main()